#include <bits/stdc++.h>
using namespace std;
int main()
{
    // for Loop
    for (int i = 1; i <= 10; i++)
    {
        cout << "Sumit Sharma" << endl;
    }

    // while loop
    int i = 1;
    while (i <= 5)
    {
        cout << "Sumit" << endl;
        i = i + 1;
    }

    // do while loop
    int i = 2;
    do
    {
        cout << "Sumit" << endl;
    } while (i <= 1);

    return 0;
}