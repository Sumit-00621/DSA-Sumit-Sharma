#include <bits/stdc++.h>
using namespace std;

int main()
{
    // int
    int a = 5; // integer

    // long
    long b = 890434; // integer with long range

    // float and double
    float c = 5.6;      // Decimals
    double d = 5.75515; // Decimals with long range

    // string and getline
    string s;
    cin >> s;
    cout << s;

    string str;
    getline(cin, str);
    cout << str;

    // char
    char ch;
    cin >> ch;
    cout << ch;

    return 0;
}